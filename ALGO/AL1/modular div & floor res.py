#MODULAR DIVISION & FLOOR RESOLUTION:

number = 123
mod_div= number % 10        # 123 / 10 = 12."3"
floor_res= number // 10     # 123 / 10 = "12".3

print(f'for number ', number)
print(f'With MODULAR DIVISION you get & use: ', mod_div)
print(f'With FLOOR RESOLUTION you get & use: ',floor_res)


